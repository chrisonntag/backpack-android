    public void openMainActivity(Account account) {
        Intent intent = new Intent(this, MainActivity.class);
        intent.putExtra(MAIN_ACCOUNT, account);
        startActivity(intent);
    }

    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mUsername;
        private final String mPassword;
        private final String mBaseUrl;
        private Context mContext;

        //Nextcloud Connection
        private OwnCloudClient ownCloudClient;
        private Handler ownCloudHandler = new Handler();

        UserLoginTask(String username, String password, String baseUrl, Context context) {
            mUsername= username.trim();
            mPassword = password;
            mBaseUrl = baseUrl;
            mContext = context;
        }

        @Override
        protected Boolean doInBackground(Void... params) {

            try {
                NextcloudAuthenticator authenticator = new NextcloudAuthenticator(mUsername, mPassword, mBaseUrl);
                authenticator.connect();
            } catch (Exception e) {
                e.printStackTrace();
                return false;
            }
            return true;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                Toast.makeText(mContext, "Successfully logged in", Toast.LENGTH_SHORT).show();

                Account account = null;
                openMainActivity(account);
            } else {
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
